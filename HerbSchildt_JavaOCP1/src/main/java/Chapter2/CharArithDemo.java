package Chapter2;

class CharArithDemo {
    public static void main(String[] args) {
        char ch;

        ch = 'X';
        System.out.println("ch contains: " + ch);

        ch++;
        System.out.println("ch incremented: " + ch);

        ch = 90;
        System.out.println("ch given the value Z: " + ch);
    }
}
