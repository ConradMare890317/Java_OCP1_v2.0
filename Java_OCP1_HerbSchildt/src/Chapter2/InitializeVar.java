public class InitializeVar {
    public static void main(String[] args) {
        int count = 10; // give count initial value of 10
        char ch = 'X'; // initialize ch with letter X
        float f = 1.2F; // f is initialized with 1.2
        
        // Declaring two or more values with a comma = one or more intial value
        int a, b = 8, c = 19, d; // b and c have initializations
    }
}
